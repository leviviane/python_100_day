fruits = ["Apple", "Peach", "Pear"]
for fruit in fruits: #will look at fruits list then assign each item to fruit
    print(fruit)
    print(fruit + " pie") #loop through list then will loop again + pie
    print(fruits) #this will run each time the for loop is ran, in this case 3 times
    #anything within the indentation will run each time loop is ran
print(fruits) #this will run once after for loop is done
